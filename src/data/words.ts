export function getWordList(difficulty: 'easy' | 'medium' | 'hard'): string[] {
	switch (difficulty) {
		case 'easy':
			return easyWords
		case 'medium':
			return mediumWords
		case 'hard':
			return hardWords
	}
}

export function generateWords(
	difficulty: 'easy' | 'medium' | 'hard',
	count: number,
): string[] {
	const list = getWordList(difficulty)
	const result: string[] = []
	for (let i = 0; i < count; i++) {
		result.push(list[Math.floor(Math.random() * list.length)])
	}
	return result
}
